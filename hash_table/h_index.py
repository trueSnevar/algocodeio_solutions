"""
H-индекс (индекс Хирша) исследователя

легко
решено

Дан массив целых чисел citations, где citations[i] — число цитирований,
полученных исследователем для его i-й статьи. Нужно найти H-индекс исследователя.

H-индекс (индекс Хирша) определяется как максимальное значение h,
при котором данный исследователь опубликовал не менее h статей,
каждая из которых была процитирована не менее h раз.

Пример 1:

Ввод:
    citations = [10,1,8,0,3]
Вывод:
    3

Объяснение:
    Существует 3 статьи с ≥ 3 цитированиями (для h=4 есть только 2 таких статьи).

Ограничения:
- len(citations) ≥ 1
- citations[i] ≥ 0
"""
from typing import List


def h_index(citations: List[int]) -> int:
    n = len(citations)
    buckets = [0] * (n + 1)

    # 1) считаем распределение цитирований
    for c in citations:
        if c >= n:
            buckets[n] += 1
        else:
            buckets[c] += 1

    # 2) накапливаем сверху вниз
    total = 0
    for i in range(n, -1, -1):
        total += buckets[i]
        if total >= i:
            return i

    return 0  # на всякий случай


if __name__ == "__main__":
    # Пример 1
    print(h_index([10, 1, 8, 0, 3]))  # 3

# Оценка сложности:
# Время: O(n log n) из-за сортировки
# Память: O(1) дополнительной памяти (или O(n), если сортировка требует буфера)
